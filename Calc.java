public class Calc {
    public int sum(int a, int b) {
        return a+b;
    }

    public int subtraction(int c, int d){
        return d-c;
    }

    public double subDouble(double c, double d){
        return d-c;
    }

    public int multi(int a, int b) {
        return a*b;
    }

    public int division(int a, int b) {
        return a/b;
    }
}
